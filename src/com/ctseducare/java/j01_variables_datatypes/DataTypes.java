package com.ctseducare.java.j01_variables_datatypes;

public class DataTypes {

    public static void main(String[] args) {

        // Numbers: Integer types
        var oneByte = 1;
        var oneShort = 1;
        var oneInt = 1;
        var oneLong = 1L;
        // byte byte_ = 128; // Erro, ultrapassa o limite de 127

        // Numbers: Floating-point types
        var eFloat = 2.7182818284f; // Float (valor assumido será 2.7182817)
        var eDouble = 2.7182818284; // Double
        System.out.println(eFloat);
        System.out.println(eDouble);

        // Boolean
        var active = true;

        // String
        var country = "Brazil";
        String state = "Paraná";

    }

}
